-- Criando as Tabelas
CREATE TABLE AVIAO
(
	IDAVIAO INT PRIMARY KEY AUTO_INCREMENT
  , MODELO VARCHAR(100)
);


CREATE TABLE AEROPORTO
(
	IDAEROPORTO INT PRIMARY KEY AUTO_INCREMENT
  , NOME VARCHAR(30)
  , SIGLA CHAR(2)
);


CREATE TABLE VOOS
(
	IDVIAGEM INT PRIMARY KEY AUTO_INCREMENT
  , ID_AVIAO INT
  , ID_PARTIDA INT
  , ID_CHEGADA INT
  , DISTANCIA INT
  , INTERCONTINENTAL BINARY
);


